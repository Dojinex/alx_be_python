#1. Simple Interest Calculator
#Define the variables
principal = 1000    #( P ) is the principal amount (initial investment),
rate = 0.05         #( R ) is the annual interest rate (as a decimal)
time = 3            #( T ) is the time the money is invested for in years.

#The formula for simple interest is (I = P * R * T), 
Interest = principal * rate * time

#Print the Result
print(f"The simple interest is: {Interest}")
